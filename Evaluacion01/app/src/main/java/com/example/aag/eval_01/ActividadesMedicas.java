public class ActividadesMedicas {

    private String date;
    private String detailsDescription;
    private String observations;
    private int num;

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getDetailsDescription() { return detailsDescription; }

    public void setDetailsDescription(String detailsDescription) { this.detailsDescription = detailsDescription; }

    public String getObservations() { return observations; }

    public void setObservations(String observations) { this.observations = observations; }



}
